public class OperadoresRelacionais {
    public static void main(String[] args) throws Exception {
        int x = 10, y = 5, z = 7, k = 7;
		System.out.println("Os operadores relacionais são usados para fazer comparações");
		System.out.println("Quando o resultado da comparação é FALSO retornam false");
		System.out.println("Quando o resultado da comparação é VERDADEIRO retornam true");
		System.out.println("Valor de x = " + x);
		System.out.println("Valor de y = " + y);
		System.out.println("Valor de z = " + z);
		System.out.println("Valor de k = " + k);
		
		System.out.println("Exemplo 1: x > y: ");
		System.out.println(x >  y);
		
		System.out.println("Exemplo 2: x < y: ");
		System.out.println(x <  y);
		
		System.out.println("Exemplo 3: z == k: ");
		System.out.println(z == k);
		
		System.out.println("Exemplo 4: z != k: ");
		System.out.println(z != k);
		
		System.out.println("Exemplo 5: z >= x: ");
		System.out.println(z >= x);
		
		System.out.println("Exemplo 6: z <= k: ");
		System.out.println(z <= k);
    }
}
